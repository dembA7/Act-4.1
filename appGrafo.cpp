#include <iostream>
#include "unweighted.h"
#include <vector>
#include <set>

int main(){
  
  // =================================================================
  // Se crea un grafo
  // =================================================================
  
  Unweighted<int> graph(5);
  
  // =================================================================
  // Se añaden sus vértices
  // =================================================================
  
  graph.addVertex(1);
  graph.addVertex(2);
  graph.addVertex(3);
  graph.addVertex(4);
  graph.addVertex(5);
  
  // =================================================================
  // Se añaden sus edges
  // =================================================================
  
  graph.addEdge(1, 2);
  graph.addEdge(1, 3);
  graph.addEdge(2, 3);
  graph.addEdge(2, 4);
  graph.addEdge(3, 4);
  graph.addEdge(3, 5);
  graph.addEdge(4, 5);

  // =================================================================
  // Se imprime el grafo
  // =================================================================
  
  std::cout << graph << std::endl;

  // =================================================================
  // DFS
  // =================================================================
  
  int start = 3;
  std::vector<int> dfs = graph.DFS(start, &graph);

  // =================================================================
  // Se imprime el DFS
  // =================================================================
  
  std::cout << "DFS from " << start << ": ";
  for (auto it = dfs.begin(); it != dfs.end(); ++it)
    std::cout << ' ' << *it;

  return 0;
}